"use strict";

// CALCULATOR FUNCTIONS

// Addition
const addition = function (num1, num2) {
  return num1 + num2;
};

// Subtraction
const subtraction = function (num1, num2) {
  return num1 - num2;
};

// Multiplication
const multiply = function (num1, num2) {
  return num1 * num2;
};

// Division
const divide = function (num1, num2) {
  return num1 / num2;
};
